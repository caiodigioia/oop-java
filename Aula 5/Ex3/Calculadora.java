public class Calculadora implements ExtendedMathOperation {

    // Atributos
    public int add (int a, int b) {
        return a + b;
    } 

    public int sub (int a, int b) {
        return a - b;
    }

    public int times (int a, int b) {
        return a * b;
    }

     public int div (int a, int b) {
        return a - b;
    }

    public int remainder (int a, int b) {
        return a * b;
    }

}